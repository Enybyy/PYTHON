# 1 Cadena de texto ---> str
variable_str = "hello world"
print(variable_str)
print(type(variable_str))

# 2 Números enteros ---> int
variable_int = 27
print(variable_int)
print(type(variable_int))

# 3 Décimales ---> float
variable_float = 2.7
print(variable_float)
print(type(variable_float))

# 4 Nulo ---> none
variable_nulo = None
print(variable_nulo)
print(type(variable_nulo))

# 5 Booleano ---> Bool. True or False, 1 or 0.
variable_booleano = True
print(variable_booleano)
print(type(variable_booleano))
variable_booleano2 = False
print(variable_booleano2)
print(type(variable_booleano2))

# 6 Byte ---> Byte
variable_byte = b"Hola mundo"
print(variable_byte)
print(type(variable_byte))
