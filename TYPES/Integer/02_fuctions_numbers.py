import math

print(round(1.2))  # Redondear
print(round(1.6))  # Redondear
print(abs(27))  # Solo número posistivos
print(abs(-27))  # solo números positivos

# Method Math
print(math.ceil(1.123))  # Redondear hacia arriba
print(math.floor(1.999))  # Redondear hacia abajo
print(math.isnan(59))  # Reconocer número
# print(math.isnan("59")) #Reconocer número
print(math.pow(10, 4))  # Elevar
print(math.sqrt(27))  # Raíz cuadrada

