# String to Integer
var_str = "0"
print(var_str)
print(type(var_str))

var_int = int(var_str)
print(var_int)
print(type(var_int))

# Integer to Boolean
var_bool = bool(var_int)
print(var_bool)
print(type(var_bool))

# Boolean to Decimal
var_float = float(var_bool)
print(var_float)
print(type(var_float))

print()
print("------------------CHANGE--------------------")
print()

# Decimal to String - Change valor
var_str = str(var_float)
var_str = "1"
print(var_str)
print(type(var_str))

var_int = int(var_str)
print(var_int)
print(type(var_int))

# Integer to Boolean
var_bool = bool(var_int)
print(var_bool)
print(type(var_bool))

# Boolean to Decimal
var_float = float(var_bool)
